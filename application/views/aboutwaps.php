        <h1>About WAPS with Simulation</h1>
        <p>Ensuring a project is planned concisely and accurately has been a struggle for most project managers and team leaders. Project Planning Techniques such as Project Evaluation and Review Technique (PERT) are used for projects with that have a rough estimate for each activity’s duration time, and Critical Path Method (CPM) for projects that have a set on activity durations, empower project managers with tools and techniques to plan, schedule, monitor, and control projects effectively, leading to successful project outcomes. However, by developing the Web-Based Automated PERT-CPM Scheduler (WAPS) with Simulation, this further improves the current calculators that are available by adding and applying more algorithms namely the Normal Distribution, Triangular Distribution, and BETA-PERT Distribution which are run together with the Monte Carlo Simulation that has a maximum of 1000 trials to achieve a more accurate activity durations and scheduling. </p>
        <p>The goals of WAPS with Simulation Web Application are the following:</p>
        <ul>
            <li>
                <p>Determine the project completion time of a project based on the durations and prerequisites of each activity.</p>
            </li>
            <li>
                <p>Calculate the slack values to know how long non-critical activities can be delayed before the overall completion date is incurred.</p>
            </li>
            <li>
                <p>Determine the critical path using the critical activities, which must be completed precisely as scheduled to meet the target for overall project completion without an extension.</p>
            </li>
            <li>
                <p>Compute the probability of an expected completion time based on the project’s computed completion time. Limited to PERT only.</p>
            </li>
            <li>
                <p>Create calculators that uses the Monte Carlo Simulation with Normal Distribution, Triangular Distribution, and BETA-PERT Distribution to each activity to achieve a competitive duration result compared to PERT.</p>
            </li>
            <li>
                <p>Generate and display a Gant Chart to provide visuals for the project flow computed by the system.</p>
            </li>
            <li>
                <p>Generate and display a PERT Chart to provide visuals of the project’s Critical Path and Pre-requisites.</p>
            </li>
            <li>
                <p>Provide an option to export the table of results in an Excel file.</p>
            </li>
            <li>
                <p>Provide an option to export the simulations ran by the Monte Carlo Simulation for the Normal Distribution, Triangular Distribution, and BETA-PERT Distribution.</p>
            </li>
        </ul>
        <p>The Web-Based Automated PERT-CPM Scheduler was developed from scratch using HTML, CSS, JavaScript, Python 3.9, and PHP 7.4 as the programming languages; CodeIgniter 3 as the chosen open-source software rapid development web framework and the functionalities available in it for fast support, cost-effective and hurdle free web development; Apache as the web server; GitHub Actions as the CI/CD Tool; and Docker as the visualization software.</p>
